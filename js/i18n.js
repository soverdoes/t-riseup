const STORAGE_KEY = 'triseup.lang';
const DEFAULT_LANG = 'ko';

function getLang() {
  try {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved && DICT[saved]) return saved;
  } catch (e) {}
  return DEFAULT_LANG;
}

function setLang(lang) {
  if (!DICT[lang]) return;
  try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
  document.documentElement.lang = lang;
  applyTranslations(lang);
  // Notify listeners (e.g. dropdown trigger labels)
  document.dispatchEvent(new CustomEvent('i18n:changed', { detail: { lang } }));
}

function t(key, lang = getLang()) {
  return (DICT[lang] && DICT[lang][key]) || (DICT[DEFAULT_LANG] && DICT[DEFAULT_LANG][key]) || key;
}

function applyTranslations(lang = getLang()) {
  // text content
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (!key) return;
    el.textContent = t(key, lang);
  });
  // attribute translations: data-i18n-attr="attr|key"
  document.querySelectorAll('[data-i18n-attr]').forEach((el) => {
    const spec = el.getAttribute('data-i18n-attr');
    if (!spec) return;
    spec.split(',').forEach((pair) => {
      const [attr, key] = pair.split('|').map(s => s.trim());
      if (attr && key) el.setAttribute(attr, t(key, lang));
    });
  });
}

// Auto-init on load
document.addEventListener('DOMContentLoaded', () => {
  const lang = getLang();
  document.documentElement.lang = lang;
  applyTranslations(lang);
});

// Expose globally
window.i18n = { t, getLang, setLang, applyTranslations };
